	//--------------------------------------------------------------------------------------------
	// 
	//--------------------------------------------------------------------------------------------
	void TUGLiGraphic::DoClearRecord()
	{
		for (s32 i = 0; i < records_.size(); ++i) Freemem(records_[i]);
		records_.clear();
	}



